#!/usr/bin/env node
// 共有の単一ソース (shared/constants/*.json) から desktop(TS) / iOS(Swift) の定数を生成する。
// 実行: `cd desktop && npm run codegen` (= node ../shared/codegen/gen-constants.mjs)。
//
// スコープ = **色プリセットのみ**。色は desktop=Tailwind クラス / iOS=RGB 手入力 で二重定義され、
// RGB ズレが目視で気づけないため単一ソース化の価値が高い。一方:
//   - timing は両 OS で値が異なる (desktop 85/400/400 vs iOS 50/200/600) ので unify しない。
//   - grid スカラー (3/9/4) は同一だが orbit/tab 配列 (両 OS で非対称) と同居 enum のため codegen は
//     侵襲的で価値が低く、対象外。
//
// 生成物は commit し、CI が `npm run codegen` 後の `git diff --exit-code` で最新性を担保する。

import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), '..', '..')
const read = (rel) => JSON.parse(readFileSync(join(repoRoot, rel), 'utf8'))

const GEN_HEADER = (src) =>
  `// AUTO-GENERATED by shared/codegen/gen-constants.mjs from ${src} — DO NOT EDIT.\n` +
  `// 値を変えるときは単一ソース (${src}) を編集して \`cd desktop && npm run codegen\` を実行する。\n`

// --- colors ---
const { colors } = read('shared/constants/colors.json')

// desktop: Tailwind クラス (bg は base+darkShade、text は 900/100 固定、light は常に -100)
function tsColors() {
  const rows = colors.map((c) => {
    const bg = `bg-${c.tailwindBase}-100 dark:bg-${c.tailwindBase}-${c.darkShade}/40`
    const text = `text-${c.tailwindBase}-900 dark:text-${c.tailwindBase}-100`
    return `  { key: '${c.key}', label: '${c.label}', bg: '${bg}', text: '${text}' },`
  })
  return (
    GEN_HEADER('shared/constants/colors.json') +
    `import type { PresetColor } from './colors'\n\n` +
    `export const PRESET_COLORS: PresetColor[] = [\n${rows.join('\n')}\n]\n`
  )
}

// iOS: RGB (Tailwind v3 値、x/255 で Color を構築)
function swiftColors() {
  const rows = colors.map((c) => {
    const [lr, lg, lb] = c.light
    const [dr, dg, db] = c.dark
    return (
      `        PresetColor(key: "${c.key}", label: "${c.label}",\n` +
      `            light: Color(red: ${lr}/255, green: ${lg}/255, blue: ${lb}/255),\n` +
      `            dark: Color(red: ${dr}/255, green: ${dg}/255, blue: ${db}/255)),`
    )
  })
  return (
    GEN_HEADER('shared/constants/colors.json') +
    `import SwiftUI\n\n` +
    `extension PresetColors {\n` +
    `    static let all: [PresetColor] = [\n${rows.join('\n')}\n    ]\n` +
    `}\n`
  )
}

const outputs = [
  ['desktop/src/constants/colors.generated.ts', tsColors()],
  ['ios/Mandalart/Utils/PresetColors.generated.swift', swiftColors()],
]

for (const [rel, content] of outputs) {
  writeFileSync(join(repoRoot, rel), content)
  console.log(`generated ${rel}`)
}
